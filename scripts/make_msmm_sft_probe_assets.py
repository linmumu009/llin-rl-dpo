#!/usr/bin/env python3
"""Create tiny MindSpeed-MM Qwen3.6 SFT probe datasets and configs."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


def make_text(repeat: int) -> str:
    unit = (
        "请阅读这段沙箱轨迹，并给出简短、稳定、可复核的操作建议。"
        "状态包括任务目标、约束、最近动作、失败原因和下一步候选。"
    )
    return unit * repeat


def write_dataset(path: Path, rows: int, repeat: int) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for idx in range(rows):
            row = {
                "images": [],
                "messages": [
                    {"role": "user", "content": f"样本 {idx}: {make_text(repeat)}"},
                    {"role": "assistant", "content": "建议先保留关键状态，确认约束，再执行最小可验证动作。"},
                ],
            }
            handle.write(json.dumps(row, ensure_ascii=False) + "\n")


