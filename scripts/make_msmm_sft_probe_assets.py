def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--dataset", default="datasets/msmm_sft_probe_long.jsonl")
    parser.add_argument("--rows", type=int, default=8)
    parser.add_argument("--repeat", type=int, default=520)
    parser.add_argument("--cutoffs", nargs="+", type=int, default=[2048, 4096])
    args = parser.parse_args()

    dataset_path = Path(args.dataset)
    write_dataset(dataset_path, args.rows, args.repeat)
    for cutoff in args.cutoffs:
        write_config(
            Path(f"configs/msmm_qwen36_sft_cutoff{cutoff}_1step.yaml"),
            cutoff,
            f"/workspace/llin-rl-dpo/{dataset_path.as_posix()}",
            f"/workspace/llin-rl-dpo/cache/msmm_sft_cutoff{cutoff}",
            f"/workspace/llin-rl-dpo/outputs/msmm-qwen36-sft-cutoff{cutoff}-1step",
        )
    print(f"wrote dataset={dataset_path} rows={args.rows} cutoffs={args.cutoffs}")


if __name__ == "__main__":
    main()
